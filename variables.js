// Variables are Containers for Storing Data

//Using var
var x = 5;
var y = 6;
var z = x + y;
console.log(z)

//Using let
let a = 5;
let b = 5;
let c = a + b;
console.log(c)


//Using const
const d = 5;
const e = 6;
const f = d + e;
console.log(f)

//Mixed
const price1 = 5;
const price2 = 6;
let total = price1 + price2;
console.log(total)

